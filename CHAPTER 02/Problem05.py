# Write a python program to find an average of two numbers entered by the user.

num1 = float(input("Enter first number: "))  # enter first number
num2 = float(input("Enter second number: "))  # enter second number
average = (num1 + num2) / 2  # calculate the average
print(f"The average of {num1} and {num2} is: {average}")  # print the average
