# Write a program to count the number of zeros in the following tuple:

# Initialize the tuple with some values including zeros
my_tuple = (0, 1, 2, 0, 3, 0, 4, 5, 0)

# Count the number of zeros in the tuple
zero_count = my_tuple.count(0)

# Print the count of zeros
print(f"The number of zeros in the tuple is: {zero_count}")
